function tecotec_a30_dummy_image_url( $width, $height, $label ) {
    return sprintf(
        'https://dummyimage.com/%dx%d/f4f6f8/146eb4.jpg&text=%s',
        absint( $width ),
        absint( $height ),
        rawurlencode( $label )
    );
}

function tecotec_a30_build_post_content( $post ) {
    $content = '';

    foreach ( $post['sections'] as $index => $section ) {
        if ( 1 === $index ) {
            $content .= sprintf(
                '<figure class="wp-block-image size-large"><img src="%s" alt="%s" loading="lazy" /><figcaption>%s</figcaption></figure>',
                esc_url( tecotec_a30_dummy_image_url( 900, 520, $post['body_image_label'] ) ),
                esc_attr( $post['body_image_label'] ),
                esc_html( $post['body_image_label'] )
            );
        }

        if ( is_array( $section ) ) {
            $content .= '<h2>' . esc_html( $section[0] ) . '</h2>';
            $content .= '<p>' . esc_html( $section[1] ) . '</p>';
        } else {
            $content .= '<p>' . esc_html( $section ) . '</p>';
        }
    }

    return $content;
}

function tecotec_a30_sideload_image( $url, $post_id, $description ) {
    require_once ABSPATH . 'wp-admin/includes/file.php';
    require_once ABSPATH . 'wp-admin/includes/media.php';
    require_once ABSPATH . 'wp-admin/includes/image.php';

    $tmp = download_url( $url );

    if ( is_wp_error( $tmp ) ) {
        return $tmp;
    }

    $file_array = array(
        'name'     => sanitize_title( $description ) . '.jpg',
        'tmp_name' => $tmp,
    );

    $attachment_id = media_handle_sideload( $file_array, $post_id, $description );

    if ( is_wp_error( $attachment_id ) ) {
        @unlink( $tmp );
        return $attachment_id;
    }

    update_post_meta( $attachment_id, '_wp_attachment_image_alt', $description );

    return $attachment_id;
}

function tecotec_a30_import_sample_posts() {
    $results = array(
        'created' => 0,
        'updated' => 0,
        'errors'  => array(),
    );

    foreach ( tecotec_a30_sample_posts_data() as $post ) {
        $category = term_exists( $post['category'], 'category' );

        if ( ! $category ) {
            $category = wp_insert_term( $post['category_label'], 'category', array( 'slug' => $post['category'] ) );
        }

        if ( is_wp_error( $category ) ) {
            $results['errors'][] = $category->get_error_message();
            continue;
        }

        $existing = get_page_by_path( $post['slug'], OBJECT, 'post' );
        $post_args = array(
            'post_title'    => $post['title'],
            'post_name'     => $post['slug'],
            'post_date'     => $post['date'],
            'post_status'   => 'publish',
            'post_type'     => 'post',
            'post_excerpt'  => $post['excerpt'],
            'post_content'  => tecotec_a30_build_post_content( $post ),
            'post_category' => array( (int) $category['term_id'] ),
            'meta_input'    => array(
                '_a30_sample_post' => '1',
                '_a30_sample_slug' => $post['slug'],
            ),
        );

        if ( $existing ) {
            $post_args['ID'] = $existing->ID;
            $post_id = wp_update_post( $post_args, true );
            $results['updated']++;
        } else {
            $post_id = wp_insert_post( $post_args, true );
            $results['created']++;
        }

        if ( is_wp_error( $post_id ) ) {
            $results['errors'][] = $post_id->get_error_message();
            continue;
        }

        global $wpdb;
        $wpdb->update( $wpdb->posts, array( 'post_status' => 'publish' ), array( 'ID' => $post_id ) );
        clean_post_cache( $post_id );

        wp_set_post_terms( $post_id, $post['tags'], 'post_tag', false );

        if ( ! has_post_thumbnail( $post_id ) ) {
            $attachment_id = tecotec_a30_sideload_image( tecotec_a30_dummy_image_url( 1200, 630, $post['image_label'] ), $post_id, $post['image_label'] );

            if ( is_wp_error( $attachment_id ) ) {
                $results['errors'][] = $attachment_id->get_error_message();
            } else {
                set_post_thumbnail( $post_id, $attachment_id );
            }
        }
    }

    return $results;
}

if ( defined( 'WP_CLI' ) && WP_CLI ) {
    WP_CLI::add_command( 'a30 import-sample-posts', function() {
        $results = tecotec_a30_import_sample_posts();

        foreach ( $results['errors'] as $error ) {
            WP_CLI::warning( $error );
        }

        WP_CLI::success( sprintf( 'A30 sample posts imported. Created: %d. Updated: %d. Errors: %d.', $results['created'], $results['updated'], count( $results['errors'] ) ) );
    } );
}
